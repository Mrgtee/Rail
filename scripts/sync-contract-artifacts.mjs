import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const buildDir = path.join(root, "contracts", "build");
const output = path.join(root, "src", "contracts", "generated.ts");
const wanted = {
  PolicyVault: "PolicyVault_PolicyVault.abi",
  AgentExecutor: "AgentExecutor_AgentExecutor.abi",
  StrategyRegistry: "StrategyRegistry_StrategyRegistry.abi",
  MockUSDC: "MockUSDC_MockUSDC.abi",
  MockRouter: "MockRouter_MockRouter.abi",
};

if (!fs.existsSync(buildDir)) {
  throw new Error("contracts/build is missing. Run npm run contracts:build first.");
}

const lines = ["// Generated by scripts/sync-contract-artifacts.mjs", "/* eslint-disable */", ""];
for (const [name, file] of Object.entries(wanted)) {
  const abiPath = path.join(buildDir, file);
  if (!fs.existsSync(abiPath)) {
    throw new Error(`Missing ABI: ${file}`);
  }
  const abi = JSON.parse(fs.readFileSync(abiPath, "utf8"));
  lines.push(`export const ${name.charAt(0).toLowerCase()}${name.slice(1)}Abi = ${JSON.stringify(abi, null, 2)} as const;`, "");
}

fs.writeFileSync(output, lines.join("\n"));
console.log(`Wrote ${path.relative(root, output)}`);
